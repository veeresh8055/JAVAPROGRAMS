public class program14 {
    
    public static void main(String[] args ){
  // prgram to finding the area and the circumference of the circle 
        double radius = 4.5;
        double pi = 3.14 ; 

        double area = pi * radius * radius ; 
        double circumference = 2 *  pi *  radius ; 

        System.out.println("area of the circle " + area); // 63.585
        System.out.println("circumference  of the circle " + circumference); // 28.26 
      
    }
}
// output
// area of the circle 63.585
// circumference  of the circle 28.26
